package com.abc.career_service.config;

import io.swagger.v3.oas.models.OpenAPI;
import io.swagger.v3.oas.models.info.Info;
import io.swagger.v3.oas.models.info.Contact;
import io.swagger.v3.oas.models.info.License;
import io.swagger.v3.oas.models.Components;
import io.swagger.v3.oas.models.security.SecurityScheme;
import io.swagger.v3.oas.models.security.SecurityRequirement;
import io.swagger.v3.oas.models.servers.Server;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

import java.util.List;

@Configuration
public class OpenApiConfig {

    @Bean
    public OpenAPI customOpenAPI() {
        return new OpenAPI()
                .info(new Info()
                        .title("Career Service API")
                        .description("Career Guidance Service for Interview System\n\n" +
                                "### üîê Authentication\n" +
                                "Use the **Authorize** button above to add your JWT token.\n" +
                                "Format: `Bearer your-jwt-token-here`\n\n" +
                                "### üìù How to Test\n" +
                                "1. Get token from Auth Service (POST /auth/login)\n" +
                                "2. Click **Authorize** button and paste token\n" +
                                "3. Token will be saved for all requests\n" +
                                "4. Use **Try it out** on any endpoint")
                        .version("1.0.0")
                        .contact(new Contact()
                                .name("Interview System Team")
                                .email("admin@interview-system.com"))
                        .license(new License()
                                .name("MIT License")
                                .url("https://opensource.org/licenses/MIT")))
                .servers(List.of(
                        new Server()
                                .url("http://localhost:8084")
                                .description("Local Development Server"),
                        new Server()
                                .url("http://localhost:8080")
                                .description("API Gateway (Recommended)")
                ))
                .components(new Components()
                        .addSecuritySchemes("bearer-jwt", new SecurityScheme()
                                .type(SecurityScheme.Type.HTTP)
                                .scheme("bearer")
                                .bearerFormat("JWT")
                                .in(SecurityScheme.In.HEADER)
                                .name("Authorization")
                                .description("JWT token for authentication. Format: Bearer {token}")))
                .addSecurityItem(new SecurityRequirement().addList("bearer-jwt"));
    }
}
